package sg.edu.nus.iss.readingcompanion.model;

public class Notes {
    private String bookId;

    private String text;
    
    public Notes() {
    }

    public String getBookId() {
        return bookId;
    }

    public void setBookId(String bookId) {
        this.bookId = bookId;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
    
    
}
